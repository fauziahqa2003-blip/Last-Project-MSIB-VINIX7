import pandas as pd
from sklearn.preprocessing import MinMaxScaler

# Baca data asli
df = pd.read_excel('coba2.xlsx')
data_mentah = df[['Daya tampung', 'UKT Mandiri']]

# Normalisasi seperti di Orange: [0, 1]
scaler = MinMaxScaler()
X = scaler.fit_transform(data_mentah)

import matplotlib.pyplot as plt
from sklearn.cluster import KMeans

wcss = []  # Within-Cluster Sum of Squares
K_range = range(1, 11)  # Uji dari 1 sampai 10 cluster

for k in K_range:
    kmeans = KMeans(n_clusters=k, random_state=42, n_init=10, max_iter=300)
    kmeans.fit(X)
    wcss.append(kmeans.inertia_)

# Plot hasil Elbow
plt.figure(figsize=(8, 5))
plt.plot(K_range, wcss, marker='o', linestyle='-', color='blue')
plt.title('Elbow Method')
plt.xlabel('Jumlah Cluster (k)')
plt.ylabel('WCSS')
plt.xticks(K_range)
plt.grid(True)
plt.show()

from sklearn.metrics import silhouette_score
from sklearn.cluster import KMeans
import matplotlib.pyplot as plt

silhouette_scores = [0]  # k=1 tidak valid, jadi kita beri nilai 0
k_values = range(2, 11)

print("Silhouette Scores per k:")
print("k=1: 0.000")  # Cetak nilai dummy untuk k=1

# Mulai dari k=2
for k in k_values:
    kmeans = KMeans(n_clusters=k, random_state=42, n_init=10, max_iter=300)
    labels = kmeans.fit_predict(X)
    score = silhouette_score(X, labels)
    silhouette_scores.append(score)
    print(f"k={k}: {score:.3f}")  # Cetak nilai silhouette

# Jalankan KMeans final dengan k terbaik
k = 6
kmeans_final = KMeans(n_clusters=k, random_state=42, n_init=10, max_iter=300)
