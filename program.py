import dash
from dash import dcc, html
import pandas as pd
import plotly.express as px
import plotly.graph_objects as go

# Membaca data
df = pd.read_excel('hasil_clustering_univ_prodi.xlsx')

# Menghitung proporsi prodi per cluster
proporsi_prodi = df['Cluster'].value_counts(normalize=True).reset_index()
proporsi_prodi.columns = ['Cluster', 'Proporsi']

# Menghitung jumlah prodi per universitas dalam tiap cluster
jumlah_prodi_univ = df.groupby(['Cluster', 'Universitas']).size().unstack(fill_value=0)

# Menghitung rata-rata tiap variabel per cluster
cluster_avg = df.groupby('Cluster')[['Daya tampung', 'UKT Mandiri']].mean()

# Layout aplikasi
app = dash.Dash(__name__)

app.layout = html.Div([
    dcc.Tabs([
        dcc.Tab(label='Clustering', children=[
            html.Div([
                html.H1("Hasil Clustering"),
                dcc.Graph(
                    figure=px.scatter(df, x='Daya tampung', y='UKT Mandiri', color='Cluster',
                                      title='Visualisasi Hasil Clustering K-Means')
                )
            ])
        ]),
        dcc.Tab(label='Rata-rata UKT Mandiri per Cluster', children=[
            html.Div([
                dcc.Graph(
                    figure=px.bar(df.groupby('Cluster')['UKT Mandiri'].mean().reset_index(),
                                  x='Cluster', y='UKT Mandiri', title='Rata-rata UKT Mandiri per Cluster')
                )
            ])
        ]),
        dcc.Tab(label='Rata-rata Daya Tampung per Cluster', children=[
            html.Div([
                dcc.Graph(
                    figure=px.bar(df.groupby('Cluster')['Daya tampung'].mean().reset_index(),
                                  x='Cluster', y='Daya tampung', title='Rata-rata Daya Tampung per Cluster')
                )
            ])
        ]),
        dcc.Tab(label='Proporsi Prodi per Cluster', children=[
            html.Div([
                dcc.Graph(
                    figure=px.pie(proporsi_prodi, values='Proporsi', names='Cluster',
                                  title='Proporsi Prodi per Cluster')
                )
            ])
        ]),
        dcc.Tab(label='Jumlah Prodi per Universitas dalam Tiap Cluster', children=[
            html.Div([
                dcc.Graph(
                    figure=go.Figure(data=[
                        go.Bar(name=univ, x=jumlah_prodi_univ.index, y=jumlah_prodi_univ[univ])
                        for univ in jumlah_prodi_univ.columns
                    ]).update_layout(title='Jumlah Prodi per Universitas dalam Tiap Cluster',
                                     barmode='stack')
                )
            ])
        ]),
        dcc.Tab(label='Karakteristik Rata-rata Tiap Cluster', children=[
            html.Div([
                dcc.Graph(
                    figure=go.Figure(data=[
                        go.Scatterpolar(
                            r=cluster_avg.loc[i].tolist() + [cluster_avg.loc[i].tolist()[0]],  # Close the circle
                            theta=cluster_avg.columns.tolist() + [cluster_avg.columns.tolist()[0]],
                            fill='toself',
                            name=f'Cluster {i}'
                        ) for i in cluster_avg.index
                    ]).update_layout(
                        title='Karakteristik Rata-rata Tiap Cluster',
                        polar=dict(radialaxis=dict(visible=True)),
                        showlegend=True
                    )
                )
            ])
        ]),
        dcc.Tab(label='Korelasi Antar Fitur per Cluster', children=[
            html.Div([
                dcc.Graph(
                    figure=px.imshow(df.groupby('Cluster')[['Daya tampung', 'UKT Mandiri']].corr(),
                                     title='Korelasi Antar Fitur per Cluster',
                                     color_continuous_scale='Viridis')
                )
            ])
        ])
    ])
])

# Menjalankan aplikasi
if __name__ == '__main__':
    app.run(debug=True)
